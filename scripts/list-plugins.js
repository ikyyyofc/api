const fs = require('fs');
const path = require('path');

function listPlugins() {
  const pluginsDir = path.join(__dirname, '..', 'plugins');
  
  console.log('\nüìã Available Plugins\n');
  
  try {
    if (!fs.existsSync(pluginsDir)) {
      console.log('‚ùå Plugins directory not found');
      return;
    }
    
    const files = fs.readdirSync(pluginsDir);
    const pluginFiles = files.filter(file => file.endsWith('.js'));
    
    if (pluginFiles.length === 0) {
      console.log('üìÅ No plugins found');
      return;
    }
    
    pluginFiles.forEach((file, index) => {
      try {
        const pluginPath = path.join(pluginsDir, file);
        const plugin = require(pluginPath);
        
        console.log(`${index + 1}. ${plugin.name || file}`);
        console.log(`   üìù ${plugin.description || 'No description'}`);
        console.log(`   üè∑Ô∏è  Version: ${plugin.version || 'N/A'}`);
        console.log(`   üìÅ File: ${file}`);
        console.log(`   üõ£Ô∏è  Routes: ${plugin.routes?.length || 0}`);
        
        if (plugin.routes && plugin.routes.length > 0) {
          plugin.routes.forEach(route => {
            console.log(`      ${route.method} ${route.path}`);
          });
        }
        
        console.log('');
      } catch (error) {
        console.log(`${index + 1}. ${file} (‚ùå Error loading)`);
        console.log(`   Error: ${error.message}\n`);
      }
    });
    
  } catch (error) {
    console.error('‚ùå Error reading plugins directory:', error.message);
  }
}

listPlugins();